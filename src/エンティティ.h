	public:
		システムサービス(エンティティサービス& エンティティサービス, レンダリングサービス& レンダラー, 入力サービス& 入力):
			エンティティサービス_(エンティティサービス),
			レンダリングサービス_(レンダラー), 
			入力_(入力){}
		~システムサービス() {}

		// エンティティから呼び出し
		エンティティサービス& エンティティ取得() { return エンティティサービス_; }
		レンダリングサービス& レンダラー取得() { return レンダリングサービス_; }
		入力サービス& 入力取得() { return 入力_; }
